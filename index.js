    const inlineMessageTimers = new WeakMap();

    function setInlineMessage(target, text, autoHide = false) {
        if (!target) {
            return;
        }

        const existingTimer = inlineMessageTimers.get(target);
        if (existingTimer) {
            clearTimeout(existingTimer);
            inlineMessageTimers.delete(target);
        }

        target.classList.remove('show');

        if (!text) {
            target.classList.remove('profile-toast');
            target.textContent = '';
            return;
        }

        target.textContent = text;

        if (!autoHide) {
            target.classList.remove('profile-toast');
            return;
        }

        target.classList.add('profile-toast');
        void target.offsetWidth;
        target.classList.add('show');

        const fadeTimer = setTimeout(() => {
            target.classList.remove('show');
            const clearTimer = setTimeout(() => {
                if (!target.classList.contains('show')) {
                    target.classList.remove('profile-toast');
                    target.textContent = '';
                }
                inlineMessageTimers.delete(target);
            }, 600);
            inlineMessageTimers.set(target, clearTimer);
        }, 1800);

        inlineMessageTimers.set(target, fadeTimer);
    }

