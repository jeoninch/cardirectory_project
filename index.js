function parseProfileHubHash(rawHash) {
    const hash = String(rawHash || window.location.hash || '').trim();
    if (!hash.startsWith(PROFILE_HUB_HASH_PREFIX)) {
        return null;
    }

    const suffix = hash.slice(PROFILE_HUB_HASH_PREFIX.length);
    const clean = suffix.startsWith('/') ? suffix.slice(1) : suffix;
    const parts = clean.split('/').filter(Boolean);
    if (parts.length === 0 || parts[0] === 'profiles') {
        const detailId = parts[1] ? decodeURIComponent(parts[1]) : '';
        return { tab: 'profiles', detailId };
    }
    if (parts[0] === 'my-profile') {
        return { tab: 'my-profile', detailId: '' };
    }
    return { tab: 'profiles', detailId: '' };
}

function buildProfileHubHash(tab, detailId = '') {
    if (tab === 'my-profile') {
        return `${PROFILE_HUB_HASH_PREFIX}/my-profile`;
    }
    if (detailId) {
        return `${PROFILE_HUB_HASH_PREFIX}/profiles/${encodeURIComponent(detailId)}`;
    }
    return `${PROFILE_HUB_HASH_PREFIX}/profiles`;
}

function setProfileHubHash(tab, detailId = '') {
    const nextHash = buildProfileHubHash(tab, detailId);
    if (window.location.hash !== nextHash) {
        window.location.hash = nextHash;
    }
}

function clearProfileHubHash() {
    if (!window.location.hash.startsWith(PROFILE_HUB_HASH_PREFIX)) {
        return;
    }
    const cleanUrl = `${window.location.pathname}${window.location.search}`;
    window.history.replaceState(window.history.state, '', cleanUrl);
}

