async function loadProfileFromFirestore(userName) {
    if (!firestoreDb) {
        return null;
    }

    try {
        const docRef = firestoreDb.collection(FIRESTORE_PROFILES_COLLECTION).doc(userName);
        const snapshot = await docRef.get();
        if (!snapshot.exists) {
            return null;
        }
        const data = snapshot.data() || {};
        const movesSnapshot = await docRef.collection('moves').get();
        const moves = [];
        movesSnapshot.forEach((moveDoc) => {
            const moveData = moveDoc.data() || {};
            const link = String(moveData.link || moveData.url || '').trim();
            if (!link) {
                return;
            }
            moves.push({
                id: moveDoc.id,
                title: String(moveData.title || moveDoc.id),
                url: link,
                description: String(moveData.description || ''),
                createdAt: String(moveData.createdAt || '')
            });
        });
        moves.sort((a, b) => b.createdAt.localeCompare(a.createdAt));
        return {
            ...data,
            moves
        };
    } catch (error) {
        console.error('Failed to load profile from Firestore:', error);
        return null;
    }
}

