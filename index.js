function normalizeProfileNameForCompare(input) {
    return String(input || '')
        .normalize('NFKC')
        .toLowerCase()
        .replace(/[^\p{L}\p{N}]+/gu, '');
}

function getNameEditDistance(a, b) {
    const left = String(a || '');
    const right = String(b || '');
    const rows = left.length + 1;
    const cols = right.length + 1;
    const dp = Array.from({ length: rows }, () => new Array(cols).fill(0));

    for (let i = 0; i < rows; i += 1) {
        dp[i][0] = i;
    }
    for (let j = 0; j < cols; j += 1) {
        dp[0][j] = j;
    }

    for (let i = 1; i < rows; i += 1) {
        for (let j = 1; j < cols; j += 1) {
            const cost = left[i - 1] === right[j - 1] ? 0 : 1;
            dp[i][j] = Math.min(
                dp[i - 1][j] + 1,
                dp[i][j - 1] + 1,
                dp[i - 1][j - 1] + cost
            );
        }
    }

    return dp[left.length][right.length];
}

function findSimilarProfileName(candidateName, existingNames) {
    const normalizedCandidate = normalizeProfileNameForCompare(candidateName);
    if (!normalizedCandidate) {
        return '';
    }

    for (const existingName of existingNames) {
        const normalizedExisting = normalizeProfileNameForCompare(existingName);
        if (!normalizedExisting) {
            continue;
        }
        if (normalizedCandidate === normalizedExisting) {
            return String(existingName || '');
        }

        const shortestLength = Math.min(normalizedCandidate.length, normalizedExisting.length);
        if (
            shortestLength >= 4 &&
            (normalizedCandidate.includes(normalizedExisting) || normalizedExisting.includes(normalizedCandidate))
        ) {
            return String(existingName || '');
        }

        const distance = getNameEditDistance(normalizedCandidate, normalizedExisting);
        if (
            (shortestLength <= 5 && distance <= 1) ||
            (shortestLength >= 6 && distance <= 2)
        ) {
            return String(existingName || '');
        }
    }

    return '';
}

function normalizeProfileSocialUrl(input, platform) {
    const value = String(input || '').trim();
    if (!value) {
        return '';
    }

    const normalized = /^https?:\/\//i.test(value) ? value : `https://${value}`;
    if (platform === 'instagram') {
        return /^https?:\/\/(www\.)?instagram\.com\/.+/i.test(normalized) ? normalized : '';
    }
    if (platform === 'youtube') {
        return /^https?:\/\/(www\.)?(youtube\.com|youtu\.be)\/.+/i.test(normalized) ? normalized : '';
    }
    return '';
}

