async function updateMoveInFirestore(userName, currentMoveId, nextTitle, nextUrl, nextDescription, nextCreatedAt) {
    if (!firestoreDb) {
        return false;
    }

    try {
        const profileRef = firestoreDb.collection(FIRESTORE_PROFILES_COLLECTION).doc(userName);
        const oldRef = profileRef.collection('moves').doc(currentMoveId);
        const oldSnap = await oldRef.get();
        const oldData = oldSnap.exists ? (oldSnap.data() || {}) : {};

        const newMoveId = toMoveDocId(nextTitle);
        const newRef = profileRef.collection('moves').doc(newMoveId);
        const now = new Date().toISOString();

        const batch = firestoreDb.batch();
        batch.set(newRef, {
            link: nextUrl,
            description: String(nextDescription || oldData.description || ''),
            createdAt: normalizeCreatedAtInput(nextCreatedAt || oldData.createdAt || now),
            updatedAt: now
        });

        if (newMoveId !== currentMoveId) {
            batch.delete(oldRef);
        }

        await batch.commit();
        return true;
    } catch (error) {
        console.error('Failed to update move in Firestore:', error);
        return false;
    }
}

